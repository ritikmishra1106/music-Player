package com.ritik.musicplayer;

public class MusicFiles {
    String path;
    String title;
    String artist;
    String album;
    String duration;
    String id;

   public MusicFiles(String path,String title,String artist,String album,String duration,String id){
       this.path=path;
       this.title=title;
       this.artist=artist;
       this.album=album;
       this.duration=duration;
       this.id=id;
   }
}
